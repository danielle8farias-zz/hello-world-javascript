function clicar(){
    let area_clique = document.getElementById('area')
    area_clique.innerText = 'Clicou!'
    area_clique.style.background = 'red'
}

function entrar(){
    let area_clique = document.getElementById('area')
    area_clique.innerText = 'Entrou!'
}

function sair(){
    let area_clique = document.getElementById('area')
    area_clique.innerText = 'Saiu!'
    area_clique.style.background = '#7736a1'
}
